class UsersController < ApplicationController
  def new
    @user = User.new
    2.times {@user.addresses.build}
  end

  def create
    @user = User.new(safe_params)
    if @user.save
      flash[:success] = "User created"
      sign_in(@user)
      redirect_to products_path
    else
      flash[:error] = "Could not create new user"
      render :new
    end
  end































































































































































































end
